    private void cancelarVueloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancelarVueloMouseClicked
        // TODO add your handling code here:
        if (!this.registroList.isSelectionEmpty()) {
            asientoSelected = ((Registro) registroListObject.get(registroList.getSelectedIndex())).getAsiento();
            asientoSelected.setDisponible(true);
            ((Registro) registroListObject.get(registroList.getSelectedIndex())).setStatus(false);
            new GestorInformacion("registro", "w", registroListObject) {

                @Override
                public void didGetData(Lista datos) {
                    new GestorInformacion("asiento") {

                        @Override
                        public void didGetData(Lista datos) {
                            for (int i = 0; i < datos.size(); i++) {
                                if (asientoSelected.getId() == ((Asiento) datos.get(i)).getId()) {
                                    datos.getNode(i).setDato(asientoSelected);
                                }
                            }
                            guardarAsientos(datos);

                        }

                        @Override
                        public void didFailLoad(String message) {

                        }

                    };

                }

                @Override
                public void didFailLoad(String message) {
                    throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                }
            };

        }

    }//GEN-LAST:event_cancelarVueloMouseClicked

    private void cedulaBtnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cedulaBtnSearchMouseClicked
        // TODO add your handling code here:
        registroModel.removeAllElements();
        for (int i = 0; i < this.registroListObject.size(); i++) {
            if (this.cedulaTxtField.getText().compareTo(((Registro) registroListObject.get(i)).getCliente().getCedula()) == 0) {
                registroModel.addElement(((Registro) registroListObject.get(i)).getCliente().getCedula() + " - " + ((Registro) registroListObject.get(i)).getAsiento().getName() + " - " + ((Registro) registroListObject.get(i)).getHora().getHora() + " - " + ((Registro) registroListObject.get(i)).getHora().getAvion().getNombre() + " - " + ((Registro) registroListObject.get(i)).getStatus());
            }
        }
    }//GEN-LAST:event_cedulaBtnSearchMouseClicked

